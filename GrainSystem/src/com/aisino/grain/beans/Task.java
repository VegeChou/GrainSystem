package com.aisino.grain.beans;

public class Task {
	private String TaskNumber = null;
	private String TaskState = null;
	private String CreateDate = null;
	private String EnrollNumber = null;
	private String GoodsOwner = null;
	private String VehiclePlate = null;
	private int GrainType = 0;
	private String GrainTypeName = null;
	private String SampleNumber = null;
	public String getTaskNumber() {
		return TaskNumber;
	}
	public void setTaskNumber(String taskNumber) {
		TaskNumber = taskNumber;
	}
	public String getTaskState() {
		return TaskState;
	}
	public void setTaskState(String taskState) {
		TaskState = taskState;
	}
	public String getCreateDate() {
		return CreateDate;
	}
	public void setCreateDate(String createDate) {
		CreateDate = createDate;
	}
	public String getEnrollNumber() {
		return EnrollNumber;
	}
	public void setEnrollNumber(String enrollNumber) {
		EnrollNumber = enrollNumber;
	}
	public String getGoodsOwner() {
		return GoodsOwner;
	}
	public void setGoodsOwner(String goodsOwner) {
		GoodsOwner = goodsOwner;
	}
	public String getVehiclePlate() {
		return VehiclePlate;
	}
	public void setVehiclePlate(String vehiclePlate) {
		VehiclePlate = vehiclePlate;
	}
	public int getGrainType() {
		return GrainType;
	}
	public void setGrainType(int grainType) {
		GrainType = grainType;
	}
	public String getGrainTypeName() {
		return GrainTypeName;
	}
	public void setGrainTypeName(String grainTypeName) {
		GrainTypeName = grainTypeName;
	}
	public String getSampleNumber() {
		return SampleNumber;
	}
	public void setSampleNumber(String sampleNumber) {
		SampleNumber = sampleNumber;
	}
	
}
