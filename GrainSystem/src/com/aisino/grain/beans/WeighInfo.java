package com.aisino.grain.beans;

public class WeighInfo {
	private int GrossWeight = 0;
	private int TareWeight = 0;
	private int NetWeight = 0;
	private int DeductWeight = 0;
	public int getGrossWeight() {
		return GrossWeight;
	}
	public void setGrossWeight(int grossWeight) {
		GrossWeight = grossWeight;
	}
	public int getTareWeight() {
		return TareWeight;
	}
	public void setTareWeight(int tareWeight) {
		TareWeight = tareWeight;
	}
	public int getNetWeight() {
		return NetWeight;
	}
	public void setNetWeight(int netWeight) {
		NetWeight = netWeight;
	}
	public int getDeductWeight() {
		return DeductWeight;
	}
	public void setDeductWeight(int deductWeight) {
		DeductWeight = deductWeight;
	}
	
}
