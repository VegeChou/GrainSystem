package com.aisino.grain.beans;

public class VehicleInfo {
	private String VehiclePlate = null;
	private String VehicleDriver = null;
	private int VehiceType = 0;
	private String VehicleTag = null;
	private String ShipPlate = null;
	public String getVehiclePlate() {
		return VehiclePlate;
	}
	public void setVehiclePlate(String vehiclePlate) {
		VehiclePlate = vehiclePlate;
	}
	public String getVehicleDriver() {
		return VehicleDriver;
	}
	public void setVehicleDriver(String vehicleDriver) {
		VehicleDriver = vehicleDriver;
	}
	public int getVehiceType() {
		return VehiceType;
	}
	public void setVehiceType(int vehiceType) {
		VehiceType = vehiceType;
	}
	public String getVehicleTag() {
		return VehicleTag;
	}
	public void setVehicleTag(String vehicleTag) {
		VehicleTag = vehicleTag;
	}
	public String getShipPlate() {
		return ShipPlate;
	}
	public void setShipPlate(String shipPlate) {
		ShipPlate = shipPlate;
	}
	
}
